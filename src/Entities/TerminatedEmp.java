package Entities;

public class TerminatedEmp {
	String teid;
	String tename;
	String tepno;
	String teemail;
	String tejobtitle;
	String edstart;
	String edend;
	public String getTeid() {
		return teid;
	}
	public void setTeid(String teid) {
		this.teid = teid;
	}
	public String getTename() {
		return tename;
	}
	public void setTename(String tename) {
		this.tename = tename;
	}
	public String getTepno() {
		return tepno;
	}
	public void setTepno(String tepno) {
		this.tepno = tepno;
	}
	public String getTeemail() {
		return teemail;
	}
	public void setTeemail(String teemail) {
		this.teemail = teemail;
	}
	public String getTejobtitle() {
		return tejobtitle;
	}
	public void setTejobtitle(String tejobtitle) {
		this.tejobtitle = tejobtitle;
	}
	public String getEdstart() {
		return edstart;
	}
	public void setEdstart(String edstart) {
		this.edstart = edstart;
	}
	public String getEdend() {
		return edend;
	}
	public void setEdend(String edend) {
		this.edend = edend;
	}
	
	
}
